//https://www.geeksforgeeks.org/problems/addition-of-two-numbers0812/1
public class AdditionOfTwoNumbers {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(addition(a,b));
    }
    static int addition(int a, int b) {
        return a+b;
    }
}
